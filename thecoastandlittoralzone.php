<li>The littoral zone is the near shore area where 
sunlight penetrates sediments, allowing for 
aquatic life to flourish, but faces rapid continuous 
change. </li>
<li>This zone consists of a backshore (inland limit of 
the beach; waves only reach this area during 
exceptionally high tides), the foreshore (the part of a shore between high and low-water marks, or between the water and cultivated or developed land.), nearshore (region close to the sea) and offshore (the sea away from 
land). </li>
<li> Coasts can be identified using longer term criteria. For instance, they can be classified using 
their geology (rocky coast, muddy coast, sandy coast etc.) and changes to sea level. Shorter 
term processes can also classify coasts depending on their inputs from rivers, waves and 
tides (e.g. Barrier coasts, Arctic coasts). 
</li>
<li>Rocky coasts are characterised by their erosional features. 
The main erosional processes are: mechanical wave erosion, 
abrasion, weathering, bio-erosion and mass movements by rock 
falls, slides and flows. </li>
<li>These types of coasts are usually in a high energy
environment where erosion can continuously occur. 
</li>
<li>In comparison, coastal plain landscapes (sandy and estuarine) 
are found in low relief areas. They result from the supply of 
sediment from offshore sources like rivers. </li>
